@router.get("/projects/{project}/search", response_class=HTMLResponse)
def search_project(request: Request, project: str, q: str = "") -> Response:
    if not request.session.get("authed"):
        return RedirectResponse("/login", status_code=303)
    safe_project = safe_slug(project, fallback="project")
    results = []
    if q:
        escaped = q.replace('"', '""')
        with get_conn() as conn:
            # Query body or title
            rows = conn.execute(
                """
                SELECT path, title, snippet(chapter_fts, 4, '<mark class="bg-accent/20 text-accent px-0.5 rounded">', '</mark>', '...', 64) as excerpt
                FROM chapter_fts
                WHERE chapter_fts MATCH ? AND project = ?
                ORDER BY rank LIMIT 50
                """,
                (f'"{escaped}"', safe_project),
            ).fetchall()

            for r in rows:
                p = Path(r["path"])
                results.append(
                    {"filename": p.name, "title": r["title"], "excerpt": r["excerpt"]}
                )

    templates = get_templates()
    return templates.TemplateResponse(
        "search.html",
        {"request": request, "project": safe_project, "q": q, "results": results},
    )


